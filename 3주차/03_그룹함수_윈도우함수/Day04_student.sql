-- ============================================================
-- 1. 윈도우 함수 (Window Function)
-- ============================================================
-- 순위, 집계 등 행과 행 사이의 관계를 정의하는 함수
-- OVER() 구문을 반드시 포함해야 한다
-- SQLite 3.25.0+ / MySQL 8.0.22+ / MariaDB 10.2.0+ 에서 지원
--
-- 기본 문법
--   SELECT WINDOW_FUNCTION(ARGUMENTS)
--          OVER (
--              [PARTITION BY 컬럼]   전체 집합을 소그룹으로 나누는 기준
--              [ORDER BY 컬럼]       소그룹에 대한 정렬 기준
--              [WINDOWING 절]        행에 대한 범위 기준
--          )
--   FROM 테이블명;
--
-- WINDOWING 절 주요 키워드
--   ROWS                            물리적 단위로 행의 집합을 지정
--   UNBOUNDED PRECEDING             윈도우 시작 위치 = 첫 번째 행
--   UNBOUNDED FOLLOWING             윈도우 마지막 위치 = 마지막 행
--   CURRENT ROW                     윈도우 시작 위치 = 현재 행
-- ============================================================


